TEST_F(CborUserConfigurationCodecTest, ValidInput)
{
    const uint8_t input[] = {0xa1, 0x01, 0x82, 0xa3, 0x01, 0x50, 0x8a, 0xc0, 0x1e, 0x5f, 0x62, 0x4e,
                             0x40, 0xdf, 0x99, 0x65, 0xe2, 0x04, 0xc9, 0xa9, 0x64, 0xe9, 0x02, 0x50,
                             0x88, 0x7e, 0x8c, 0xb0, 0xe8, 0x72, 0x4a, 0x39, 0xbf, 0x75, 0x00, 0xa3,
                             0x14, 0x12, 0x00, 0x99, 0x03, 0x50, 0xAA, 0x7e, 0x8c, 0xb0, 0xe8, 0x72,
                             0x4a, 0x39, 0xbf, 0x75, 0x00, 0xa3, 0x14, 0x12, 0x00, 0xBB, 0xa3, 0x01,
                             0x50, 0x8a, 0xc0, 0x1e, 0x5f, 0x62, 0x4e, 0x40, 0xdf, 0x99, 0x65, 0xe2,
                             0x04, 0xc9, 0xa9, 0x64, 0xe9, 0x02, 0x50, 0x88, 0x7e, 0x8c, 0xb0, 0xe8,
                             0x72, 0x4a, 0x39, 0xbf, 0x75, 0x00, 0xa3, 0x14, 0x12, 0x00, 0x99, 0x03,
                             0x50, 0xAA, 0x7e, 0x8c, 0xb0, 0xe8, 0x72, 0x4a, 0x39, 0xbf, 0x75, 0x00,
                             0xa3, 0x14, 0x12, 0x00, 0xBB};

    etl::array<LayoutConfigurationDescriptor, 2> expectedLayoutConfigurationDescriptors{
        LayoutConfigurationDescriptor{uuid::Uuid(idLayoutConfiguration),
                                      uuid::Uuid(layoutConfigurationHash),
                                      uuid::Uuid(layoutHash)},
        LayoutConfigurationDescriptor{uuid::Uuid(idLayoutConfiguration),
                                      uuid::Uuid(layoutConfigurationHash),
                                      uuid::Uuid(layoutHash)}};

    UserConfiguration userConfiguration(id);

    bool ret = codec.decode({reinterpret_cast<const std::byte*>(input), sizeof(input)},
                            userConfiguration);

    EXPECT_TRUE(ret);

    auto layoutConfigurationList = userConfiguration.getLayoutConfigurationList();
    EXPECT_EQ(layoutConfigurationList.size(), expectedLayoutConfigurationDescriptors.size());

    for(size_t i = 0; i < layoutConfigurationList.size(); ++i)
    {
        EXPECT_EQ(layoutConfigurationList[i].id, expectedLayoutConfigurationDescriptors[i].id);
        EXPECT_EQ(layoutConfigurationList[i].layoutConfigurationHash,
                  expectedLayoutConfigurationDescriptors[i].layoutConfigurationHash);
        EXPECT_EQ(layoutConfigurationList[i].layoutHash,
                  expectedLayoutConfigurationDescriptors[i].layoutHash);
    }
}

